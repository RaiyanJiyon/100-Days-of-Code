# Built-in function: prints "Hello" to the console
print("Hello")

# Built-in function: calculates the length of the string "Hello"
length = len("Hello")
print(length)

# User-defined function: defines a function named my_function
def my_function():
    print("Hello")
    print("Bye")

# Calling the user-defined function my_function
# This will execute the print statements within the function
my_function()
