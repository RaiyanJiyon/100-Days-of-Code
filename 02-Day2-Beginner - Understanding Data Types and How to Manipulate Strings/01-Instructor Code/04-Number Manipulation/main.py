# Printing the result of division (floating-point)
print(8 / 3)

# Printing the result of division (integer part)
print(int(8 / 3))

# Printing the result of division (rounded)
print(round(8 / 3))

# Printing the result of division (rounded to 2 decimal places)
print(round(8 / 3, 2))

# Initializing the variable 'score' with the value 10
score = 10

# Incrementing the value of 'score' by 1
score += 1

# Printing the updated value of 'score'
print(score)
