two_digit_number = input()  # Taking input of a two-digit number

# Extracting the first and second digits from the input
first_digit = int(two_digit_number[0])
second_digit = int(two_digit_number[1])

# Calculating the sum of the two digits
sum_of_digits = first_digit + second_digit

# Printing the sum
print(sum_of_digits)
